// Operadores Aritiméticos/Comparação/Lógicos

// Os operadores aritméticos são usados para realizar operações matemáticas.
//  Aqui estão os principais operadores aritméticos em JavaScript

let a = 10
let b = 5

console.log(a + b); // 15
console.log(a - b); // 5
console.log(a * b); // 50
console.log(a / b); // 2
console.log(a % b); // 0
console.log(a ** 2); // 100

// Operadores de Comparação
// Os operadores de comparação são usados para comparar dois valores
// e retornam um valor booleano (true ou false).
// Aqui estão os principais operadores de comparação em JavaScript:

let x = 10
let y = 10

console.log(x == y); // True
console.log(x === y); // True
console.log(x != y); // False
console.log(x !== y); // False
console.log(x > y); // False
console.log(x < y); // False

// Operadores Lógicos
// Os operadores lógicos são usados para combinar expressões booleanas.
// Aqui estão os principais operadores lógicos em JavaScript:
let cond1 = true
let cond2 = false

console.log(cond1 && cond2); // False
console.log(cond1 || cond2); // True
console.log(!cond1); // false




